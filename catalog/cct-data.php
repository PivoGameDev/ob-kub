function fmtPrice($p) {
    if ($p >= 1000000) return number_format($p / 1000000, 1, '.', '') . ' млн ₽';
    if ($p >= 1000) return number_format($p / 1000, 0, '.', '') . ' тыс ₽';
    return number_format($p, 0, '.', ' ') . ' ₽';
}