def first_word(phr):
    p = phr.strip('"').strip().lower()
    stop = {"купить", "заказать", "цена", "стоимость", "прайс", "где", "нужен", "нужна", "ищу",
            "изготовление", "производство", "недорого", "срочно", "аналог", "замена",
            "импортозамещение", "каталог"}
    words = p.split()
    if words and words[0] in stop:
        return words[1] if len(words) > 1 else words[0]
    return words[0] if words else p

